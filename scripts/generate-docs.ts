// ── ABI types + signature rendering ────────────────────────────────────

interface AbiParam {
    name: string;
    type: string;
    internalType?: string;
    indexed?: boolean;
    components?: AbiParam[];
}

interface AbiItem {
    type: 'function' | 'event' | 'error' | 'constructor' | 'receive' | 'fallback';
    name?: string;
    inputs?: AbiParam[];
    outputs?: AbiParam[];
    stateMutability?: 'pure' | 'view' | 'nonpayable' | 'payable';
}

function paramType(p: AbiParam): string {
    if (p.internalType) {
        // "struct Patron.Listing" → "Patron.Listing"; keep array suffixes.
        return p.internalType.replace(/^(struct|enum|contract) /, '');
    }
    return p.type;
}

function renderParams(params: AbiParam[] | undefined, withIndexed = false): string {
    if (!params || params.length === 0) return '';
    return params
        .map((p) => {
            const indexed = withIndexed && p.indexed ? ' indexed' : '';
            const name = p.name ? ` ${p.name}` : '';
            return `${paramType(p)}${indexed}${name}`;
        })
        .join(', ');
}

function functionSignature(item: AbiItem): string {
    const mut = item.stateMutability;
    const mutStr = mut === 'nonpayable' ? '' : ` ${mut}`;
    const outs = item.outputs && item.outputs.length > 0 ? ` returns (${renderParams(item.outputs)})` : '';
    return `function ${item.name}(${renderParams(item.inputs)}) external${mutStr}${outs}`;
}

function eventSignature(item: AbiItem): string {
    return `event ${item.name}(${renderParams(item.inputs, true)})`;
}

function errorSignature(item: AbiItem): string {
    return `error ${item.name}(${renderParams(item.inputs)})`;
}

/** Disambiguation key used by prose for overloads: name(type1,type2). */
function overloadKey(item: AbiItem): string {
    return `${item.name}(${(item.inputs ?? []).map((p) => p.type).join(',')})`;
}

// ── Prose parsing ──────────────────────────────────────────────────────

interface ProseBlock {
    kind: 'function' | 'event' | 'error' | 'receive';
    key: string; // name or name(types)
    access: string | null;
    body: string;
    order: number;
}

interface ProseFile {
    frontmatter: Record<string, string>;
    summary: string;
    concepts: string | null;
    blocks: ProseBlock[];
}

function parseFrontmatter(src: string): {frontmatter: Record<string, string>; body: string} {
    const m = src.match(/^---\n([\s\S]*?)\n---\n?/);
    if (!m) return {frontmatter: {}, body: src};
    const frontmatter: Record<string, string> = {};
    for (const line of m[1].split('\n')) {
        const kv = line.match(/^(\w[\w-]*):\s*(.*)$/);
        if (kv) frontmatter[kv[1]] = kv[2].trim();
    }
    return {frontmatter, body: src.slice(m[0].length)};
}

function parseProse(path: string): ProseFile {
    const {frontmatter, body} = parseFrontmatter(readFileSync(path, 'utf8'));
    // Split into segments on top-level `# ` and `## ` headings. `###`+ stay inside bodies.
    const lines = body.split('\n');
    interface Seg {
        level: number;
        heading: string;
        body: string[];
    }
    const segs: Seg[] = [];
    let cur: Seg | null = null;
    let inFence = false;
    for (const line of lines) {
        if (/^```/.test(line)) inFence = !inFence;
        const h = !inFence && line.match(/^(#{1,2}) (.+)$/);
        if (h) {
            if (cur) segs.push(cur);
            cur = {level: h[1].length, heading: h[2].trim(), body: []};
        } else if (cur) {
            cur.body.push(line);
        } else if (line.trim() !== '') {
            throw new Error(`${path}: content before the first heading: "${line}"`);
        }
    }
    if (cur) segs.push(cur);

    let summary = '';
    let concepts: string | null = null;
    const blocks: ProseBlock[] = [];
    let order = 0;
    for (const seg of segs) {
        const text = seg.body.join('\n').trim();
        if (seg.level === 1) {
            if (seg.heading === 'summary') summary = text;
            else if (seg.heading === 'concepts') concepts = text;
            else throw new Error(`${path}: unknown top-level section "# ${seg.heading}" (only summary/concepts allowed)`);
            continue;
        }
        // level 2: item block
        if (seg.heading === 'receive') {
            const {access, rest} = extractAccess(text);
            blocks.push({kind: 'receive', key: 'receive', access, body: rest, order: order++});
            continue;
        }
        const m = seg.heading.match(/^(function|event|error) (\w+)(\([\w\s,[\]]*\))?$/);
        if (!m) throw new Error(`${path}: malformed block heading "## ${seg.heading}"`);
        const kind = m[1] as ProseBlock['kind'];
        const key = m[3] ? `${m[2]}${m[3].replace(/\s+/g, '')}` : m[2];
        const {access, rest} = extractAccess(text);
        blocks.push({kind, key, access, body: rest, order: order++});
    }
    if (!summary) throw new Error(`${path}: missing "# summary" section`);
    return {frontmatter, summary, concepts, blocks};
}

function extractAccess(text: string): {access: string | null; rest: string} {
    const m = text.match(/^access:\s*(.+)\n?/);
    if (!m) return {access: null, rest: text};
    return {access: m[1].trim(), rest: text.slice(m[0].length).trim()};
}

// ── Address substitution ───────────────────────────────────────────────

function substituteTokens(text: string, context: string): string {
    return text.replace(/\{\{addr:(\w+)\}\}/g, (_, key: string) => {
        const v = DEPLOYMENTS[key];
        if (typeof v !== 'string' || !v.startsWith('0x')) {
            throw new Error(`${context}: {{addr:${key}}} does not resolve to an address in deployments.mainnet.json`);
        }
        return v;
    });
}

// ── Contract page assembly ─────────────────────────────────────────────

interface BuiltPage {
    section: string;
    slug: string;
    title: string;
    description: string;
    markdown: string; // final body (no frontmatter)
}

interface AccessRow {
    contract: string;
    slug: string;
    fn: string;
    signature: string;
    access: string;
}

interface IndexRow {
    contract: string;
    slug: string;
    name: string;
    signature: string;
    prose: string;
}

const accessRows: AccessRow[] = [];
const errorRows: IndexRow[] = [];
const eventRows: IndexRow[] = [];

function firstSentence(text: string): string {
    const flat = text.replace(/\s+/g, ' ').trim();
    // Shield abbreviation dots so "i.e." / "e.g." don't end the sentence.
    const shielded = flat.replace(/\b(i\.e|e\.g)\./gi, '$1\u0000');
    const m = shielded.match(/^.*?[.!?](?=\s|$)/);
    return (m ? m[0] : shielded).replace(/\u0000/g, '.').slice(0, 240);
}

function buildContractPage(page: ContractPage): BuiltPage {
    const abiPath = join(ABI_DIR, `${page.name}.json`);
    const prosePath = join(PROSE_DIR, `${page.name}.md`);
    if (!existsSync(abiPath)) throw new Error(`missing ABI: ${abiPath}`);
    if (!existsSync(prosePath)) throw new Error(`missing prose supplement: ${prosePath}`);
    const abi = JSON.parse(readFileSync(abiPath, 'utf8')) as AbiItem[];
    const prose = parseProse(prosePath);

    // Index ABI items; detect overloads.
    const fns = abi.filter((i) => i.type === 'function');
    const events = abi.filter((i) => i.type === 'event');
    const errors = abi.filter((i) => i.type === 'error');
    const hasReceive = abi.some((i) => i.type === 'receive');
    const nameCounts = new Map<string, number>();
    for (const f of fns) nameCounts.set(f.name!, (nameCounts.get(f.name!) ?? 0) + 1);

    const proseFor = new Map<string, ProseBlock>();
    for (const b of prose.blocks) {
        const mapKey = `${b.kind}:${b.key}`;
        if (proseFor.has(mapKey)) throw new Error(`${prosePath}: duplicate block ${mapKey}`);
        proseFor.set(mapKey, b);
    }

    const problems: string[] = [];
    const usedBlocks = new Set<string>();

    function lookup(kind: ProseBlock['kind'], item: AbiItem): ProseBlock | null {
        const overload = overloadKey(item);
        const byOverload = proseFor.get(`${kind}:${overload}`);
        if (byOverload) {
            usedBlocks.add(`${kind}:${overload}`);
            return byOverload;
        }
        if ((nameCounts.get(item.name!) ?? 0) > 1 && kind === 'function') {
            problems.push(`overloaded function ${item.name} needs a "## function ${overload}" block`);
            return null;
        }
        const byName = proseFor.get(`${kind}:${item.name!}`);
        if (byName) {
            usedBlocks.add(`${kind}:${item.name!}`);
            return byName;
        }
        problems.push(`missing "## ${kind} ${item.name}" block`);
        return null;
    }

    // Partition + order functions: write fns in prose order, read fns alphabetical.
    const writeFns: {item: AbiItem; block: ProseBlock | null}[] = [];
    const readFns: {item: AbiItem; block: ProseBlock | null}[] = [];
    for (const f of fns) {
        const block = lookup('function', f);
        const isWrite = f.stateMutability === 'nonpayable' || f.stateMutability === 'payable';
        if (isWrite && block && !block.access) {
            problems.push(`state-changing function ${f.name} has no "access:" line`);
        }
        (isWrite ? writeFns : readFns).push({item: f, block});
    }
    writeFns.sort((a, b) => (a.block?.order ?? 1e9) - (b.block?.order ?? 1e9));
    readFns.sort((a, b) => a.item.name!.localeCompare(b.item.name!));
    events.sort((a, b) => a.name!.localeCompare(b.name!));
    errors.sort((a, b) => a.name!.localeCompare(b.name!));

    let receiveBlock: ProseBlock | null = null;
    if (hasReceive) {
        receiveBlock = proseFor.get('receive:receive') ?? null;
        if (receiveBlock) usedBlocks.add('receive:receive');
        else problems.push('missing "## receive" block for the receive() function');
        if (receiveBlock && !receiveBlock.access) problems.push('receive() block has no "access:" line');
    }

    const eventBlocks = events.map((e) => ({item: e, block: lookup('event', e)}));
    const errorBlocks = errors.map((e) => ({item: e, block: lookup('error', e)}));

    for (const key of proseFor.keys()) {
        if (!usedBlocks.has(key)) problems.push(`prose block "${key}" matches nothing in the ABI`);
    }
    if (problems.length > 0) {
        throw new Error(`${page.name}: prose/ABI mismatch\n  - ${problems.join('\n  - ')}`);
    }

    // Assemble markdown.
    const address = page.deploymentsKey ? (DEPLOYMENTS[page.deploymentsKey] as string) : null;
    const parts: string[] = [];
    parts.push(`# ${page.name}`);
    if (address) {
        parts.push(
            `\`${address}\` · [view on evm.now](https://evm.now/address/${address}?chainId=1) · deployed at block ${DEPLOYMENTS.deployBlock}`,
        );
    }
    parts.push(prose.summary);
    if (prose.concepts) {
        parts.push(`## Concepts`);
        parts.push(prose.concepts);
    }

    if (writeFns.length > 0 || hasReceive) {
        parts.push(`## Write functions`);
        for (const {item, block} of writeFns) {
            parts.push(`### ${item.name}`);
            parts.push('```solidity\n' + functionSignature(item) + '\n```');
            parts.push(`**Access:** ${block!.access}`);
            if (block!.body) parts.push(block!.body);
            accessRows.push({
                contract: page.name,
                slug: page.slug,
                fn: item.name!,
                signature: functionSignature(item),
                access: block!.access!,
            });
        }
        if (hasReceive && receiveBlock) {
            parts.push(`### receive`);
            parts.push('```solidity\nreceive() external payable\n```');
            parts.push(`**Access:** ${receiveBlock.access}`);
            if (receiveBlock.body) parts.push(receiveBlock.body);
            accessRows.push({
                contract: page.name,
                slug: page.slug,
                fn: 'receive',
                signature: 'receive() external payable',
                access: receiveBlock.access!,
            });
        }
    }

    if (readFns.length > 0) {
        parts.push(`## Read functions`);
        for (const {item, block} of readFns) {
            parts.push(`### ${item.name}`);
            parts.push('```solidity\n' + functionSignature(item) + '\n```');
            if (block!.body) parts.push(block!.body);
        }
    }

    if (eventBlocks.length > 0) {
        parts.push(`## Events`);
        for (const {item, block} of eventBlocks) {
            parts.push(`### ${item.name}`);
            parts.push('```solidity\n' + eventSignature(item) + '\n```');
            if (block!.body) parts.push(block!.body);
            eventRows.push({
                contract: page.name,
                slug: page.slug,
                name: item.name!,
                signature: eventSignature(item),
                prose: firstSentence(block!.body),
            });
        }
    }

    if (errorBlocks.length > 0) {
        parts.push(`## Errors`);
        for (const {item, block} of errorBlocks) {
            parts.push(`**\`${errorSignature(item).replace(/^error /, '')}\`**`);
            parts.push(block!.body);
            errorRows.push({
                contract: page.name,
                slug: page.slug,
                name: item.name!,
                signature: errorSignature(item),
                prose: firstSentence(block!.body),
            });
        }
    }

    const markdown = substituteTokens(parts.join('\n\n') + '\n', prosePath);
    return {
        section: 'contracts',
        slug: page.slug,
        title: prose.frontmatter.title || page.name,
        description: firstSentence(prose.summary),
        markdown,
    };
}

// ── Hand pages ─────────────────────────────────────────────────────────

function buildHandPage(hp: HandPage): BuiltPage {
    const path = join(PAGES_DIR, hp.file);
    if (!existsSync(path)) throw new Error(`missing hand-written page: ${path}`);
    const {frontmatter, body} = parseFrontmatter(readFileSync(path, 'utf8'));
    if (!frontmatter.title) throw new Error(`${path}: frontmatter needs a title`);
    const markdown = substituteTokens(body.trim() + '\n', path);
    const h1 = markdown.match(/^# (.+)$/m);
    return {
        section: hp.section,
        slug: hp.slug,
        title: frontmatter.title,
        description: frontmatter.description || (h1 ? h1[1] : frontmatter.title),
        markdown,
    };
}

