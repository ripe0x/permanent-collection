/**
 * Protocol reference-docs generator.
 *
 * Merges three inputs into the final API reference:
 *   1. Checked-in ABIs            app/lib/abis/<Name>.json      (signatures, events, errors)
 *   2. Mainnet deployments        contracts/deployments.mainnet.json (addresses)
 *   3. Hand-authored prose        docs/reference/_prose/<Name>.md
 *      + hand-written pages       docs/reference/_pages/**
 *
 * Outputs:
 *   docs/reference/<section>/<slug>.md   final pages (GENERATED — edit _prose/_pages)
 *   docs/reference/reference/*.md        cross-cutting indexes (access control, errors, events)
 *   app/lib/docs/manifest.json           sidebar tree + page metadata
 *   app/lib/docs/search-index.json       client-side search corpus
 *   app/public/abis/<Name>.json          ABI copies served to integrators/agents
 *   app/public/protocol-manifest.json    machine-readable protocol entry point
 *   app/public/llms.txt                  LLM orientation file
 *
 * The generator is deliberately strict: every ABI function/event/error must
 * have a prose block, every state-changing function must declare its access
 * model, and prose blocks naming unknown ABI items fail the build. Signatures
 * can never drift because they are derived, not written.
 *
 * Prose format: see docs/reference/_prose/SPEC.md.
 */
