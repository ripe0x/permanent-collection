/**
 * Pixel font for OG cards — the same 5×7 glyph style as the on-chain mosaic
 * renderer (mirrored in lib/mosaic-svg.ts). The first 27 glyphs are copied
 * verbatim from the on-chain table; a few extra glyphs (H V . $) are appended
 * for OG-only value cards (ETH amounts, the $111 ticker) in the same style —
 * these are NOT part of the on-chain font.
 *
 * Renders a string to a standalone, scalable SVG data URI (vector rects, crisp
 * at any size) for embedding as an <img> in next/og cards.
 */

// 7 rows/glyph, 5 bits/row. On-chain order: space, 0-9, /, A C D E F I L M N O P R S T U.
// OG-only additions after that: H V . $.
const GLYPHS = [
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // space
  0x0e, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0e, // 0
  0x04, 0x0c, 0x04, 0x04, 0x04, 0x04, 0x0e, // 1
  0x0e, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1f, // 2
  0x1e, 0x01, 0x01, 0x0e, 0x01, 0x01, 0x1e, // 3
  0x11, 0x11, 0x11, 0x1f, 0x01, 0x01, 0x01, // 4
  0x1f, 0x10, 0x10, 0x1e, 0x01, 0x01, 0x1e, // 5
  0x0e, 0x11, 0x10, 0x1e, 0x11, 0x11, 0x0e, // 6
  0x1f, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08, // 7
  0x0e, 0x11, 0x11, 0x0e, 0x11, 0x11, 0x0e, // 8
  0x0e, 0x11, 0x11, 0x0f, 0x01, 0x11, 0x0e, // 9
  0x01, 0x01, 0x02, 0x04, 0x08, 0x10, 0x10, // /
  0x0e, 0x11, 0x11, 0x1f, 0x11, 0x11, 0x11, // A
  0x0f, 0x10, 0x10, 0x10, 0x10, 0x10, 0x0f, // C
  0x1e, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1e, // D
  0x1f, 0x10, 0x10, 0x1e, 0x10, 0x10, 0x1f, // E
  0x1f, 0x10, 0x10, 0x1e, 0x10, 0x10, 0x10, // F
  0x1f, 0x04, 0x04, 0x04, 0x04, 0x04, 0x1f, // I
  0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1f, // L
  0x11, 0x1b, 0x15, 0x11, 0x11, 0x11, 0x11, // M
  0x11, 0x11, 0x19, 0x15, 0x13, 0x11, 0x11, // N
  0x0e, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0e, // O
  0x1e, 0x11, 0x11, 0x1e, 0x10, 0x10, 0x10, // P
  0x1e, 0x11, 0x11, 0x1e, 0x14, 0x12, 0x11, // R
  0x0f, 0x10, 0x10, 0x0e, 0x01, 0x01, 0x1e, // S
  0x1f, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04, // T
  0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0e, // U
  // ── OG-only additions (same style; not on-chain) ──
  0x11, 0x11, 0x11, 0x1f, 0x11, 0x11, 0x11, // H
  0x11, 0x11, 0x11, 0x11, 0x11, 0x0a, 0x04, // V
  0x00, 0x00, 0x00, 0x00, 0x00, 0x0c, 0x0c, // .
  0x04, 0x0f, 0x14, 0x0e, 0x05, 0x1e, 0x04, // $
];

function glyphIndex(c: number): number {
  if (c === 0x20) return 0; // space
  if (c >= 0x30 && c <= 0x39) return 1 + (c - 0x30); // 0..9
  if (c === 0x2f) return 11; // /
  const map: Record<number, number> = {
    0x41: 12, 0x43: 13, 0x44: 14, 0x45: 15, 0x46: 16, 0x49: 17, 0x4c: 18,
    0x4d: 19, 0x4e: 20, 0x4f: 21, 0x50: 22, 0x52: 23, 0x53: 24, 0x54: 25, 0x55: 26,
    0x48: 27, // H
    0x56: 28, // V
    0x2e: 29, // .
    0x24: 30, // $
  };
  return map[c] ?? 0;
}

export interface PixelText {
  src: string; // data URI of the SVG
  w: number; // intrinsic width (glyph units)
  h: number; // intrinsic height (glyph units)
}

/** Render `text` (uppercased) as a pixel-font SVG data URI in `color`. */
export function pixelText(text: string, color: string): PixelText {
  const t = text.toUpperCase();
  let rects = '';
  for (let i = 0; i < t.length; i++) {
    const cx = i * 6;
    const base = glyphIndex(t.charCodeAt(i)) * 7;
    for (let row = 0; row < 7; row++) {
      const bits = GLYPHS[base + row];
      if (!bits) continue;
      let runStart = 0;
      let inRun = false;
      for (let col = 0; col < 5; col++) {
        const on = ((bits >> (4 - col)) & 1) === 1;
        if (on && !inRun) {
          runStart = col;
          inRun = true;
        } else if (!on && inRun) {
          rects += `<rect x="${cx + runStart}" y="${row}" width="${col - runStart}" height="1" fill="${color}"/>`;
          inRun = false;
        }
      }
      if (inRun) {
        rects += `<rect x="${cx + runStart}" y="${row}" width="${5 - runStart}" height="1" fill="${color}"/>`;
      }
    }
  }
  const w = Math.max(1, t.length * 6 - 1);
  const h = 7;
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${w} ${h}" shape-rendering="crispEdges">${rects}</svg>`;
  return { src: `data:image/svg+xml;base64,${Buffer.from(svg).toString('base64')}`, w, h };
}
